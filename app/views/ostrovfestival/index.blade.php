	        <div class="message">
	          Organizátor si vyhradzuje právo odoprieť vstup a v prípade takéhoto odopretia preplatiť cenu vstupenky. Vstupenka je pri vstupe nahradená označením návštevníka festivalu náramkom. Za stratu, odcudzenie alebo poškodenie označenia nenesie organizátor zodpovednosť. Organizátor nenesie zodpovednosť za osobné škody, stratu a zranenia vzniknuté na podujatí, ak boli spôsobené nedbalosťou držiteľa lístka alebo nepredvídateľným správaním iných návštevníkov a tretích osôb. Vstup na festivalové pódium je pre návštevníkov zakázaný, porušenie zákazu je dôvodom na vyvedenie z areálu a odobratie identifikačného náramku. Skákanie z pódia do davu, tzv. stage diving, je na festivale zakázaný, porušenie zákazu je dôvodom na vyvedenie z areálu a odobratie identifikačného náramku. Oprávneným vstupom do areálu návštevník poskytuje svoj výslovný súhlas s tým, že fotografie, filmy alebo videozáznamy s jeho osobou, ktoré sú zhotovené počas festivalu alebo v súvislosti s ním, môžu byť použité prostredníctvom akejkoľvek súčasnej alebo budúcej technickej metódy, a to bez peňažnej náhrady a časového obmedzenia. Je prísne zakázané vnášať do priestoru konania festivalu sklenené nádoby, nádoby pod tlakom, pyrotechnické predmety, sklenené fľaše, dáždniky, predmety ohrozujúce bezpečnosť návštevníkov, zbrane akéhokoľvek druhu a zakladať otvorený oheň. Osoby vstupujúce do areálu môžu byť prehľadané. 
